/// Xaritani chegara ichida ushlab turish funksiyasi
void constrainMap(MapCamera camera, MapController mapController) {
  if (!kShowMapBorder || kFerganaBorder.isEmpty) return;

  final bounds = LatLngBounds.fromPoints(kFerganaBorder);

  if (!bounds.contains(camera.center)) {
    WidgetsBinding.instance.addPostFrameCallback((_) {
      try {
        mapController.move(const LatLng(40.3864, 71.7825), camera.zoom);
      } catch (e) {
        debugPrint("Map constrain error: $e");
      }
    });
  }
}
